import * as utils from "ipv4-calculator/dist/utils.js"

function serviceURLtoIPNumber(serviceURL) {
    var ip = ""
    // Remove http://
    ip = serviceURL.replace("http://", "")
    // Remove :8081
    ip = ip.replace(":8081", "")
    return utils.default.ipToNumber(ip)
}

nodes.sort(function(firstNode, secondNode) {
    var firstIP = serviceURLtoIPNumber(firstNode.service_url)
    var secondIP = serviceURLtoIPNumber(secondNode.service_url)
    if (firstIP < secondIP) {
        return -1
    } else if (firstIP > secondIP) {
        return 1
    } else {
        return 0
    }
})

// create an array with all ips
const numberedIps = nodes.map(function(node) {
    return serviceURLtoIPNumber(node.service_url)
})

// Create an array with all the missing IP's
const missingIPs = numberedIps.map(function(numberedIP) {
    const nextIP = numberedIP + 1
    if (!numberedIps.includes(nextIP)) {
        return utils.default.ipNumberToIp(nextIP)
    }
})

